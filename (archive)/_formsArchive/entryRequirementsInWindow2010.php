				    <td align="left" valign="top" class="entryRequirementsText"><span style="color:#FFFF66">Acceptance Criteria. </span> We're
				      interested in work that integrates dance and cinematography. Works may include live dance performance in a mixed-media context with dance cinema. 
              When choosing works, we consider production values,
				      thoughtful forms and themes, investigative / innovative / experimental approaches, audience appeal, and how the piece
				      will fit with
				      or complement others we are considering. None of these criteria is a must; none are more important than the others;
				      excellence in any one or two areas may be sufficient for acceptance. Shorts are preferred. Documentaries and
				      animations will be considered. Note that we are not interested in
				      simple recordings of dance on a proscenium stage &#8212; cinematic elements must be an integral part of the entry. </td>
