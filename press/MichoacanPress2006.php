            <td height="10" colspan="3" valign="top"  class="bodyTextOnBlack"><div class="programInfoTextSmall">In Bellas Artes, International Festival of
                Videodance</div><br>
                Starting yesterday and through next Friday the International Sans Souci Film Festival of Dance Cinema will
                be taking place at the Universidad Michoacana. The festival is organized by the Escuela Popular de Bellas
                Artes and sponsored by the University of Boulder in Colorado, USA.<br>              
                <br>
                Roc&iacute;o Luna, who coordinated the event, explained that Sans Souci seeks to question traditional definitions
                of dance, video and videodance, and to work as a platform for creative and experimental works to take form.
                Since its inception in 2003, Sans Souci has been dedicated to finding and promting new talent as well as<br>
                showcasing artists with established careers.<br>              
                <br>
                A chief objective of the festival is to motivate interdisciplinary collaboration amongst students of dance,
                video and art. Videodance, a hybrid by nature, is an excellent example of such artistic collaborations.<br>              
                <br>
                Today the festival will feature a screening of La Semilla Santa followed by a workshop of modern dance in
                the afternoon. Renowned dancers Ana Baer and Dani Beauchamp will participate in the festival, as well as
                composer Randy Gibson.<br>
                <br>
                Viridiana L&oacute;pez<br>
                Cambio de Michoac&aacute;n, October 24, 2006<br>
