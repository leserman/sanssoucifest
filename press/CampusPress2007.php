            <td height="10" colspan="3" valign="top"  class="bodyTextOnBlack">
                  Video installations, film and live dance filled the Boulder Museum of Contemporary Art (BMoCA) Saturday
                    night for the Sans Souci Festival of Dance Cinema.<br><br>
                  Returning for its fourth year, Sans Souci brought out fans of dance cinema to the two-night festival. Different
                  videos were showcased each night, although some were shown both Friday and Saturday. In addition to the
                  films, the festival also included live dance performances and video installations.<br><br>
                  Sans Souci, which means &quot;no worries&quot; in French, was created four years ago by Michelle Ellsworth
                  and friend Brandi Mathis as they sat on the porch of a mobile home in the Sans Souci Trailer Park in South
                  Boulder. Ellsworth, a dance teacher at CU, initially thought that Sans Souci would be a night when people
                  gathered at the trailer park to see what everyone was making. The idea started off small, but with the
                  help of Mathis who was working at BMoCA and Ellsworth at CU, as well as artists Ana Baer and Hamel Bloom,
                  Sans Souci became much larger than originally planned.<br><br>
                  Showing 11 films each night, the festival incorporated films of every type, most of which were of the more
                  avant-garde style. The night started off with the film &quot;Bleu&quot; by Cari Ann Shim Sham which was
                  so well received the first night that it was added to Saturday night's show.<br><br>
                  The four-minute-long film started with a nude figure in black and white jerkily moving her body. Bright
                  blue post-it notes slowly started accumulating on her, producing a scaly effect. As described in the program, &quot;'Bleu'
                  explores the politics of a working class woman through surrealistic imagery and organic movement.&quot;<br><br>
                  Next was &quot;City Crumbs,&quot; by Sans Souci Artistic Co-Director Ana Baer. Baer, who currently lives
                  in Los Angeles, filmed the movie in both Denver and D&uuml;sseldorf, Germany. &quot;City Crumbs&quot; was
                  divided into four parts, all of them having a moving background image of graffiti.<br><br>
                  &quot;
                  I wanted it to include something urban,&quot; Baer said about the film. &quot;And I wanted a live component.&quot;<br><br>
                  The two dancers performed live dressed in chic costumes made of newspaper. Baer said the piece was about
                  relationships and how hard it is to have one in big cities.<br><br>
                  Baer, also the curator of the films, received double the number of submissions as last year. Out of about
                  55 submissions, only 25 were chosen for the festival and four were foreign.<br><br>
                  &quot;
                  I wanted to honor what we do,&quot; Baer said. &quot;There's a broad definition of video dance.&quot;<br><br>
                  Baer said that video dance is not a documentary of the dance, it's about the film and the dance itself.
                  Some films can be more on the dance side, whereas some are more experimental. Baer said the film &quot;Plutoian
                  Blueprint&quot; was the most experimental of the night.<br><br>
                  &quot;
                  Some people might say that 'Plutoian Blueprint' isn't dance at all,&quot; Baer said. &quot;But it's work
                  created for the camera.&quot;<br><br>
                  Hamel Bloom, executive director of Sans Souci, said all of the films include dance, but none of them look
                  like someone pointed a camera at a stage during a dance performance.<br><br>
                  &quot;
                  Some are photo realistic, others are abstract,&quot; Bloom said about the films.<br><br>
                  This was the first year the festival included video installations, which were well received. Baer expects
                  to make the installations a bigger part of next year's festival.<br><br>
                  Ellsworth and Baer have been showing films from previous Sans Souci festivals in other countries, touring
                  through Mexico and Germany last year.<br><br>
                  &quot;
                  It's so easy to get this stuff seen once it's been curated,&quot; Ellsworth said.<br><br>
                  The film that ended both nights was &quot;CEA&quot; by Canadian filmmaker Robert Prowse.<br><br>
                  &quot;
                  It's a very beautiful film showing women dancing in a field of grain,&quot; Bloom said. &quot;It's almost
                  as if they're part of the field of grain. Their movement blends with it.&quot;<br><br>
