                              <div class="programInfoText" style="margin:10px 0 10px;">Colorado hosts Sans Souci Festival for Dance Cinema</div>
                              <div class="bodyTextOnBlack" style="margin-bottom:12px;">by Chrysta Brown, 09/16/11</div>
                              <div>
                                <div style="font-weight:normal;">
                                  Dancers are taught to work hard in the studio so that one day they can dance on stage. And when the four walls of the stage become constricting, where do you take your art next? To the screen. If the world is a stage, than the universe is a screen, and the Sans Souci Festival is using this weekend to celebrate them both.<br><br>
                                  Sans Souci, translated "without concern," traces its beginnings to a conversation between Michelle Ellsworth and Brandi Mathis on a mobile home porch in the Sans Souci Trailer Park in Boulder. With the support of the Boulder Museum of Contemporary Art and The University of Colorado, Ellsworth, along with dance and film enthusiasts, Ana Baer and Hamel Bloom turned that conversation into an event. In 2003 several dance for film artists gathered inside a trailer for an informal showing of their work.<br><br>
                                  Now, eight years later, location, in more ways than one, is the biggest difference. Sans Souci has moved from its trailer beginnings and to Boulder Museum of Contemporary Art and the University of Boulder. Additionally, it has expanded its events both nationally and internationally, hosting screenings in Texas, California, Mexico, and multiple countries in Europe. While we can still refer to it as a showing between friends, those friends are no longer limited to the borders of Colorado. Sans Souci receives submissions from all over the world, including some of the most prominent dance for film choreographers.<br><br>
                                  The films in the Sans Souci Festival tell stories through dance. What separates this festival from other festivals of its kinds is that it lives at the intersection between dance cinema and live performance. It isn't simply a viewing, it is a dance experience.<br><br>
                                  The Sans Souci Festival will be presented on September 16 -17, 2011 at the University of Colorado at Boulder's Atlas Building, with longer showings at the Boulder Public Library on October 3, 5, 10, 12. For more information, including a detailed program visit: <a href="http://sanssoucifest.org">http://sanssoucifest.org</a>. Admission is free of charge, early seat reservation is recommended.<br>
