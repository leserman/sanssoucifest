			    Sans Souci (meaning &quot;without concern&quot;) was conceived one fine spring day in 2003 when <a href="http://michelleellsworth.com">Michelle
			      Ellsworth</a> and Brandi Mathis sat  on the 
          porch of a 1967 Marlette Mobile home in the Sans Souci Trailer Park in  Boulder Colorado musing about the pleasures
			      of viewing and creating dances for the screen. Quickly <a href="http://www.bmoca.org">BMoCA</a> and The University of Colorado as well as artists <a href="http://avantmedia.org/artists/principal.html">Ana
          Baer</a> and Hamel Bloom added their support to transform mere 
          musing into a Festival of Dance Cinema.  What was first envisioned in '03 as an informal gathering of local
          dance video artists screening their works on a white wall in a trailer is now an international festival housed
          at BMoCA with submissions from all over the world.<br>
          <br>
          Sans Souci Festival of Dance Cinema started in Boulder Colorado in
                        2003 with the support of the <a href="http://www.bmoca.org/">Boulder Museum of Contemporary Art</a> (BMoCA)
                        and the <a href="http://www.colorado.edu/TheatreDance/">University of Colorado at Boulder</a>. With
                        an expansive definition of <i>dance</i> and an appreciation for highly experimental and interdisciplinary
                        forms, this unique festival exposes diverse audiences to a variety of film, video, and performance
                        possibilities.</div>
